package com.capgemini;

public class Calculator {
	
	public int add (int var1, int var2) {
		System.out.println("Adding " + var1 + " + " + var2);
		return var1 + var2;
		}

	
	public int subtract (int var1, int var2) {
		System.out.println("Subtracting " + var1 + " + " + var2);
		return var1 - var2;
		}

}
